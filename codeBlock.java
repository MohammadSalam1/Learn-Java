//the use of a code block
public class codeBlock {
    public static void main(String[] args) {
        double i, j, d;

        i = 5;
        j = 10;

        //target of this (if) is a block
        if (i != 0) {
            System.out.println("i does not equal zero");
            d = j / i;
            System.out.println("j / i is " + d);
        }
    }
}
