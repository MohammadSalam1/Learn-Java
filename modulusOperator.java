public class modulusOperator {
    public static void main(String[] args) {

        int results, irem;
        double dresults, drem;

        results = 10 / 3;
        irem = 10 % 3;

        dresults = 10 / 3;
        drem = 10 % 3;

        System.out.println("Results and remainders of 10 / 3: " + results + " " + irem);
        System.out.println("Results and remainders of 10 / 3: " + dresults + " " + drem);

    }
}
