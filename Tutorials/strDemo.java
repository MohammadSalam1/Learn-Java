//escape sequences
public class strDemo {
    public static void main(String[] args) {

        System.out.println("first line \nsecond line"); // \n whatever output comes after will be in a new line
        System.out.println("A \tB \tC");
        System.out.println("D \tE \tF"); // the \t is a tap space to align output

    }
}
